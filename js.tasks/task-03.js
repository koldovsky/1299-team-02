function giveMeFive(obj){
  let resultArr = [];
  for(const value in obj){
    if(value.length === 5){
      resultArr.push(value);
    }
    if(obj[value].length === 5){
      resultArr.push(obj[value]);
    }
  }
  return resultArr;
}

